<?php
session_start();
require_once '../config/database.php';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $email = $_POST['email'];
    $password = $_POST['password'];
    
    try {
        $stmt = $pdo->prepare("
            SELECT h.*, d.name as department_name 
            FROM hod h
            JOIN departments d ON d.id = h.department_id
            WHERE h.email = ?
        ");
        $stmt->execute([$email]);
        $hod = $stmt->fetch();
        
        if ($hod && password_verify($password, $hod['password'])) {
            $_SESSION['hod_id'] = $hod['id'];
            $_SESSION['hod_name'] = $hod['name'];
            $_SESSION['department_id'] = $hod['department_id'];
            $_SESSION['department_name'] = $hod['department_name'];
            
            header("Location: dashboard.php");
            exit();
        } else {
            $error = "Invalid email or password";
        }
    } catch (PDOException $e) {
        $error = "Error: " . $e->getMessage();
    }
}
